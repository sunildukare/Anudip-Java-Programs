package fundamentals;

public class ArithmeticOperators {
	/*
	 * * ---> Multiplication
	 * + ---> Addition
	 * - ---> Subtraction
	 * / ---> Numbers are divided and quotient given as answer
	 * % ---> Numbers are divided and remainded given as answer
	 */
	
	public static void main(String[] args) {
		int num1 = 5;
		int num2 = 2;
		int x = num1/num2;
		int y = num1%num2;
		System.out.println("x= "+x);
		System.out.println("y= "+y);
	}
}
