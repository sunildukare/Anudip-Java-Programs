package statementsAndLoops;

public class IfElseDemo1 {

	public static void main(String[] args) {
		/*
		 * if else is a type of statement.
		 * We use it to put constraints or conditions on our code.
		 * 
		 * if block of codes will execute only when the condition in if is satisfied.
		 * otherwise else is executed.
		 */
		double marks=45;
		
		if (marks>=60) {
			System.out.println("I am happy!!!");
		}
		else {
			System.out.println("I am sad!!!");
		}
	}
}
