package StringPrograms;

public class Demo1 {
	public static void main(String[] args) {
		String name = "Akash Badgujar";
		int len = name.length();
		System.out.println("Number of characters in name = "+len);
		char ch = name.charAt(10);
		System.out.println("The character on 10th index of name = "+ch);
	}
}
